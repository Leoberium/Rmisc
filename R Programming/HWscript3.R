names(unlist(l))

# Далее в тексте: list() = список

# Левое поддерево
l[[1]] # Как элемент (этот элемент - список)
l[1] # Как подсписок, содержащий нужный элемент (тоже список)

# Узел с листьями b и c
l[[1]][[2]] # Как элемент (этот элемент - список)
l[[1]][2] # Как подсписок, содержащий нужный элемент (тоже список)
